    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
        // TODO add your handling code here:
        String titulo = jTextFieldTitulo.getText();
        var director = jComboBoxDirector.getSelectedItem();
        String fechaProyec = jTextFieldFecha.getText();
        String añoEstreño = jTextFieldAñoEstreno.getText();
        var tematica = jComboBoxTematica.getSelectedItem();
        String precioEntrada = jTextFieldPrecio.getText();
        var sala = jComboBoxSala.getSelectedItem();
        
        String[] idDirector= director.toString().split("-");
        String Did=idDirector[0];
        
        String[] idTematica= tematica.toString().split("-");
        String Teid=idTematica[0];
        
        String[] idSala= sala.toString().split("-");
        String Said=idSala[0];
        
        
        

        
        PreparedStatement ps=null;
        conectar = new Conectar();
        Connection conexion = conectar.getConexion();
        
        String sql= "insert into pelicula(titulo,director,fechaProyeccion,añoEstreno,tematica,precioEntrada,sala) "
                + "values ('"+titulo+"','"+Did+"','"+fechaProyec+"','"+añoEstreño+"','"+Teid+"','"+precioEntrada+"','"+Said+"')";
        
        try {
          ps = conexion.prepareStatement(sql);
          ps.executeUpdate();
            
        } catch (SQLException ex) {
            Logger.getLogger(AñadirDirector.class.getName()).log(Level.SEVERE, null, ex);
        }

        dispose();
    }//GEN-LAST:event_jButtonInsertActionPerformed

 
